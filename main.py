print("Buenas, el siguiente código le pedirá dos números con el proposito de sumarlos, restarlos, multiplicarlos y dividirlos entre sí y en el orden que usted de.")
primer_numero = float(input())
segundo_numero = float(input())

print("El resultado de sumar", primer_numero,
      "y", segundo_numero, "es:", primer_numero + segundo_numero)

print("El resultado de restar", primer_numero,
      "menos", segundo_numero, "es:", primer_numero - segundo_numero)

print("El resultado de multiplicar", primer_numero,
      "y", segundo_numero, "es:", primer_numero * segundo_numero)

print("El resultado de dividir", primer_numero,
      "entre", segundo_numero, "es:", primer_numero / segundo_numero)
